import java.io.*;

class penjumlahan{
	public static void main (String[] args){
		double aa=5,bb=6,cc=0.5,tb,kl,bg;
		
		tb=aa+bb;
		kl=aa*bb*cc;
		bg=bb/cc;
		
		System.out.println("aa + bb = "+tb);
		System.out.println("aa x bb x cc = "+kl);
		System.out.println("bb / cc = "+bg);
	}
}